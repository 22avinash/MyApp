function divmouseover(){
	document.getElementById('div_form').style.boxShadow="2px 2px 10px 2px #888888";
	document.getElementById('div_form').style.fontSize="12";
}
function divmouseout(){
	document.getElementById('div_form').style.boxShadow="2px 2px 10px .2px #888888";
	document.getElementById('div_form').style.fontSize="10";
}